    <h3>Stefan Bennedahl</h3>
    Keramik
<p>Stefan föddes 1954 i Värnamo. Han har gått kurser i skulptur och keramik och arbetat med skulptur i över 30 år.
Stefan har arbetat som konstnär på heltid sedan 2009. Om sin konst säger Stefan, "Mina skulpturer skildrar människans inre, oftast mörka, värld".&nbsp;
Som en del av Blå Huset har Stefan sitt personliga projekt Röda Rummet.
En totalrenoverad utställningslokal, främst för konstnärens egen keramik. 
